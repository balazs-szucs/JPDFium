package stirling.software.jpdfium.model;

/**
 * Position for placing images on PDF pages.
 */
public enum Position {

    /** Top-left corner */
    TOP_LEFT,

    /** Top center */
    TOP_CENTER,

    /** Top-right corner */
    TOP_RIGHT,

    /** Middle left */
    MIDDLE_LEFT,

    /** Center of the page */
    CENTER,

    /** Middle right */
    MIDDLE_RIGHT,

    /** Bottom-left corner */
    BOTTOM_LEFT,

    /** Bottom center */
    BOTTOM_CENTER,

    /** Bottom-right corner */
    BOTTOM_RIGHT
}
